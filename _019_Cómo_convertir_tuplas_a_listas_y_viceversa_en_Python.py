#Como convertir tuplas a listas y viceversa en Python

lista=['negro','blanco','rosa','nigga'] #string
tupla= tuple(lista) #variable y convertor de lista a tupla 
print(tupla) #imprecion 

tupla2=('mole','pollo','col','pinole') #string
lista2= list(tupla2) #variable y convertor de tupla a lista
print(lista2) #imprecion 